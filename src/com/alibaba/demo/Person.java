package com.alibaba.demo;

public class Person {

    /*年龄*/
    private int age;
    /*性别*/
    private char sex;
    /*性别*/
    private String name;

    public int getAge() {
        return age;
    }

    public char getSex() {
        return sex;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setSex(char sex) {
        this.sex = sex;
    }

    public void setName(String name) {
        this.name = name;
    }
}
